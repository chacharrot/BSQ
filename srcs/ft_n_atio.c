#include "header.h"

int				ft_n_atoi(char *str, int end)
{
	int num;

	num = 0;
	while (*str != '\0' && 0 <= end)
	{
		num = num * 10 + (*str - '0');
		str++;
		end--;
	}
	return (num);
}
